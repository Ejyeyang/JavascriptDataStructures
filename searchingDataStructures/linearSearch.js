
/**
 * linear search is when you search
 * from beginning to end. 
 */

//returns index of target 
function linearSearch(arr, target){
    for(let i = 0; i < arr.length; i++){
        if(arr[i] === target){
            return i;
        }
    }
    return -1; 
}
